/*A set of Instructions to be processed in one time.*/
package org.museframework.bian.classes;

public class InstructionBatch {
    /*An identifier that allows to uniquely refer to an Instruction Batch file. */
    private String batchIdentifier;

    public void setBatchIdentifier(String batchIdentifier) {
        this.batchIdentifier = batchIdentifier;
    }

    public String getBatchIdentifier() {
        return batchIdentifier;
    }
}