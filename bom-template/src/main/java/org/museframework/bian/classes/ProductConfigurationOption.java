/*Options for configuring the product or the function/feature of product.*/
package org.museframework.bian.classes;

public class ProductConfigurationOption {
    /**/
    private String optionDefinition;

    /**/
    private String optionSetting;

    public void setOptionDefinition(String optionDefinition) {
        this.optionDefinition = optionDefinition;
    }

    public String getOptionDefinition() {
        return optionDefinition;
    }

    public void setOptionSetting(String optionSetting) {
        this.optionSetting = optionSetting;
    }

    public String getOptionSetting() {
        return optionSetting;
    }
}