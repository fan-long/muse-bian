/*The action or activity of gathering information about target markets or customers.*/
package org.museframework.bian.classes;

public class MarketResearch {
    /*Refers to information (e.g. Market research report) used in the Market Research
*/
    private String marketResearchSource;

    /*Refers to the viewpoint used in the market research

*/
    private String marketResearchViewpoint;

    /*Refers to the approach used in the market research
*/
    private String marketResearchApproach;

    /*Period covered by the market research*/
    private String marketResearchPeriod;

    /*Market that is subject of the market research

*/
    private String researchedMarket;

    public void setMarketResearchSource(String marketResearchSource) {
        this.marketResearchSource = marketResearchSource;
    }

    public String getMarketResearchSource() {
        return marketResearchSource;
    }

    public void setMarketResearchViewpoint(String marketResearchViewpoint) {
        this.marketResearchViewpoint = marketResearchViewpoint;
    }

    public String getMarketResearchViewpoint() {
        return marketResearchViewpoint;
    }

    public void setMarketResearchApproach(String marketResearchApproach) {
        this.marketResearchApproach = marketResearchApproach;
    }

    public String getMarketResearchApproach() {
        return marketResearchApproach;
    }

    public void setMarketResearchPeriod(String marketResearchPeriod) {
        this.marketResearchPeriod = marketResearchPeriod;
    }

    public String getMarketResearchPeriod() {
        return marketResearchPeriod;
    }

    public void setResearchedMarket(String researchedMarket) {
        this.researchedMarket = researchedMarket;
    }

    public String getResearchedMarket() {
        return researchedMarket;
    }
}