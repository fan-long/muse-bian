package org.museframework.bian.classes;

public class Campaign {
    /*Type of campaign.*/
    private String campaignType;

    /*The version of campaign.*/
    private String campaignVersion;

    /**/
    private String campaignDescription;

    /**/
    private String campaignPeriod;

    public void setCampaignType(String campaignType) {
        this.campaignType = campaignType;
    }

    public String getCampaignType() {
        return campaignType;
    }

    public void setCampaignVersion(String campaignVersion) {
        this.campaignVersion = campaignVersion;
    }

    public String getCampaignVersion() {
        return campaignVersion;
    }

    public void setCampaignDescription(String campaignDescription) {
        this.campaignDescription = campaignDescription;
    }

    public String getCampaignDescription() {
        return campaignDescription;
    }

    public void setCampaignPeriod(String campaignPeriod) {
        this.campaignPeriod = campaignPeriod;
    }

    public String getCampaignPeriod() {
        return campaignPeriod;
    }
}