/*Metric that is defined to assess the impact of the promotional event*/
package org.museframework.bian.classes;

public class PromotionalEventImpactMetric {
    /*A description of the metric, defining how it is to be measured during and after the event*/
    private String promotionalEventImpactMetricDefinition;

    /*A description of the target performance for the metric*/
    private String promotionalEventImpactMetricGoal;

    public void setPromotionalEventImpactMetricDefinition(String promotionalEventImpactMetricDefinition) {
        this.promotionalEventImpactMetricDefinition = promotionalEventImpactMetricDefinition;
    }

    public String getPromotionalEventImpactMetricDefinition() {
        return promotionalEventImpactMetricDefinition;
    }

    public void setPromotionalEventImpactMetricGoal(String promotionalEventImpactMetricGoal) {
        this.promotionalEventImpactMetricGoal = promotionalEventImpactMetricGoal;
    }

    public String getPromotionalEventImpactMetricGoal() {
        return promotionalEventImpactMetricGoal;
    }
}