/*A transaction by which one currency is exchanged for another at an agreed Exchange Rate on an agreed date.*/
package org.museframework.bian.classes;

public class CurrencyExchangeTransaction {
    /*Currency and amount bought in a foreign exchange trade. (ISO20022)
*/
    private String buyAmountAndCurrency;

    /*Currency and amount sold in a foreign exchange trade. (ISO20022)*/
    private String sellAmountAndCurrency;

    /*Amount of money resulting from a foreign exchange transaction (ISO20022)*/
    private String resultingAmount;

    public void setBuyAmountAndCurrency(String buyAmountAndCurrency) {
        this.buyAmountAndCurrency = buyAmountAndCurrency;
    }

    public String getBuyAmountAndCurrency() {
        return buyAmountAndCurrency;
    }

    public void setSellAmountAndCurrency(String sellAmountAndCurrency) {
        this.sellAmountAndCurrency = sellAmountAndCurrency;
    }

    public String getSellAmountAndCurrency() {
        return sellAmountAndCurrency;
    }

    public void setResultingAmount(String resultingAmount) {
        this.resultingAmount = resultingAmount;
    }

    public String getResultingAmount() {
        return resultingAmount;
    }
}