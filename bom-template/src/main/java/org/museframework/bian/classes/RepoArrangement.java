/*An Arrangement within the Repurchase Agreement.

Obligation covered by a repurchase agreement. (ISO20022)*/
package org.museframework.bian.classes;

public class RepoArrangement {
    /*Rate to be used to recalculate the repurchase amount. (ISO20022)*/
    private String repurchaseRate;

    /*Closing date/time or maturity date/time of the repo transaction. (ISO20022)*/
    private String terminationDateTime;

    /**/
    private String repurchaseAmount;

    /*Specifies the type of repurchase transaction. (ISO20022)*/
    private org.museframework.bian.enumerations.Repurchasetypevalues repurchaseType;

    public void setRepurchaseRate(String repurchaseRate) {
        this.repurchaseRate = repurchaseRate;
    }

    public String getRepurchaseRate() {
        return repurchaseRate;
    }

    public void setTerminationDateTime(String terminationDateTime) {
        this.terminationDateTime = terminationDateTime;
    }

    public String getTerminationDateTime() {
        return terminationDateTime;
    }

    public void setRepurchaseAmount(String repurchaseAmount) {
        this.repurchaseAmount = repurchaseAmount;
    }

    public String getRepurchaseAmount() {
        return repurchaseAmount;
    }

    public void setRepurchaseType(org.museframework.bian.enumerations.Repurchasetypevalues repurchaseType) {
        this.repurchaseType = repurchaseType;
    }

    public org.museframework.bian.enumerations.Repurchasetypevalues getRepurchaseType() {
        return repurchaseType;
    }
}