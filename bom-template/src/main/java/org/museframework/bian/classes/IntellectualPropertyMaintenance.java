/*Tracking the key maintenance tasks associated with the asset (e.g. copyright/patent processing, infringements)*/
package org.museframework.bian.classes;

public class IntellectualPropertyMaintenance {
    /*The type of maintenance task (e.g. annual renewal)*/
    private String intellectualPropertyMaintenanceTaskType;

    /**/
    private String intellectualPropertyMaintenanceTaskDescription;

    /**/
    private String intellectualPropertyMaintenanceTaskDateTime;

    /**/
    private String intellectualPropertyMaintenanceTaskDocument;

    public void setIntellectualPropertyMaintenanceTaskType(String intellectualPropertyMaintenanceTaskType) {
        this.intellectualPropertyMaintenanceTaskType = intellectualPropertyMaintenanceTaskType;
    }

    public String getIntellectualPropertyMaintenanceTaskType() {
        return intellectualPropertyMaintenanceTaskType;
    }

    public void setIntellectualPropertyMaintenanceTaskDescription(String intellectualPropertyMaintenanceTaskDescription) {
        this.intellectualPropertyMaintenanceTaskDescription = intellectualPropertyMaintenanceTaskDescription;
    }

    public String getIntellectualPropertyMaintenanceTaskDescription() {
        return intellectualPropertyMaintenanceTaskDescription;
    }

    public void setIntellectualPropertyMaintenanceTaskDateTime(String intellectualPropertyMaintenanceTaskDateTime) {
        this.intellectualPropertyMaintenanceTaskDateTime = intellectualPropertyMaintenanceTaskDateTime;
    }

    public String getIntellectualPropertyMaintenanceTaskDateTime() {
        return intellectualPropertyMaintenanceTaskDateTime;
    }

    public void setIntellectualPropertyMaintenanceTaskDocument(String intellectualPropertyMaintenanceTaskDocument) {
        this.intellectualPropertyMaintenanceTaskDocument = intellectualPropertyMaintenanceTaskDocument;
    }

    public String getIntellectualPropertyMaintenanceTaskDocument() {
        return intellectualPropertyMaintenanceTaskDocument;
    }
}