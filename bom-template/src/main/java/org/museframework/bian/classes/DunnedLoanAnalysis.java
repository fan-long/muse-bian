/*Analysis of the state of the product sales agreement that is the subject of the dunning procedure*/
package org.museframework.bian.classes;

public class DunnedLoanAnalysis {
    /**/
    private String dunnedLoanReference;

    /*Classification of the loan that is subject to the dunning procedure*/
    private String dunnedLoanType;

    /*Amount due on the dunned loan */
    private String dunnedLoanOutstandingBalance;

    /*Currency in which the Dunned Loan Amount is expressed*/
    private String dunnedLoanCurrency;

    /*Original loan amount of the dunned loan*/
    private String dunnedLoanAmount;

    /*The date on which the dunned loan agreement was agreed to end*/
    private String dunnedLoanEndDate;

    /*Arrangemnts of the loan that is subject to the dunning procedure


at the time of analysis*/
    private String dunnedLoanConfiguration;

    /*Reference to the tax regime the dunned loan is subject

at the time of the analysis*/
    private String dunnedLoanTaxregime;

    /*The status of the dunned loan

at the time of the analysis

e.g. active, overdue, in suspense...*/
    private String dunnedLoanStatus;

    public void setDunnedLoanReference(String dunnedLoanReference) {
        this.dunnedLoanReference = dunnedLoanReference;
    }

    public String getDunnedLoanReference() {
        return dunnedLoanReference;
    }

    public void setDunnedLoanType(String dunnedLoanType) {
        this.dunnedLoanType = dunnedLoanType;
    }

    public String getDunnedLoanType() {
        return dunnedLoanType;
    }

    public void setDunnedLoanOutstandingBalance(String dunnedLoanOutstandingBalance) {
        this.dunnedLoanOutstandingBalance = dunnedLoanOutstandingBalance;
    }

    public String getDunnedLoanOutstandingBalance() {
        return dunnedLoanOutstandingBalance;
    }

    public void setDunnedLoanCurrency(String dunnedLoanCurrency) {
        this.dunnedLoanCurrency = dunnedLoanCurrency;
    }

    public String getDunnedLoanCurrency() {
        return dunnedLoanCurrency;
    }

    public void setDunnedLoanAmount(String dunnedLoanAmount) {
        this.dunnedLoanAmount = dunnedLoanAmount;
    }

    public String getDunnedLoanAmount() {
        return dunnedLoanAmount;
    }

    public void setDunnedLoanEndDate(String dunnedLoanEndDate) {
        this.dunnedLoanEndDate = dunnedLoanEndDate;
    }

    public String getDunnedLoanEndDate() {
        return dunnedLoanEndDate;
    }

    public void setDunnedLoanConfiguration(String dunnedLoanConfiguration) {
        this.dunnedLoanConfiguration = dunnedLoanConfiguration;
    }

    public String getDunnedLoanConfiguration() {
        return dunnedLoanConfiguration;
    }

    public void setDunnedLoanTaxregime(String dunnedLoanTaxregime) {
        this.dunnedLoanTaxregime = dunnedLoanTaxregime;
    }

    public String getDunnedLoanTaxregime() {
        return dunnedLoanTaxregime;
    }

    public void setDunnedLoanStatus(String dunnedLoanStatus) {
        this.dunnedLoanStatus = dunnedLoanStatus;
    }

    public String getDunnedLoanStatus() {
        return dunnedLoanStatus;
    }
}