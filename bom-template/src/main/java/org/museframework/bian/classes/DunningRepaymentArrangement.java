/*Description of how the repayments on overdue accounts receivable will be done, as arranged during a dunning procedure.*/
package org.museframework.bian.classes;

public class DunningRepaymentArrangement {
    /*Time Schedule for repayment agreed upon in the dunning repayment arrangement */
    private String dunningRepaymentArrangementSchedule;

    /*individually arranged amount, to be paid on the individually agreed staged payment date

recurring pair of attributes...

*/
    private String dunningRepaymentArrangementStagedPaymentAmount;

    /*Individually arranged date, on which the individually agreed staged payment amount is due

recurring pair of attributes...*/
    private String dunningRepaymentArrangementStagedPaymentDate;

    /*Amount that needs to be paid according to the repayment schedule, as agreed in the dunning repayment arrangement*/
    private String dunningRepaymentArrangementAmount;

    public void setDunningRepaymentArrangementSchedule(String dunningRepaymentArrangementSchedule) {
        this.dunningRepaymentArrangementSchedule = dunningRepaymentArrangementSchedule;
    }

    public String getDunningRepaymentArrangementSchedule() {
        return dunningRepaymentArrangementSchedule;
    }

    public void setDunningRepaymentArrangementStagedPaymentAmount(String dunningRepaymentArrangementStagedPaymentAmount) {
        this.dunningRepaymentArrangementStagedPaymentAmount = dunningRepaymentArrangementStagedPaymentAmount;
    }

    public String getDunningRepaymentArrangementStagedPaymentAmount() {
        return dunningRepaymentArrangementStagedPaymentAmount;
    }

    public void setDunningRepaymentArrangementStagedPaymentDate(String dunningRepaymentArrangementStagedPaymentDate) {
        this.dunningRepaymentArrangementStagedPaymentDate = dunningRepaymentArrangementStagedPaymentDate;
    }

    public String getDunningRepaymentArrangementStagedPaymentDate() {
        return dunningRepaymentArrangementStagedPaymentDate;
    }

    public void setDunningRepaymentArrangementAmount(String dunningRepaymentArrangementAmount) {
        this.dunningRepaymentArrangementAmount = dunningRepaymentArrangementAmount;
    }

    public String getDunningRepaymentArrangementAmount() {
        return dunningRepaymentArrangementAmount;
    }
}