/*An arrangement defining the applicable features for a savings account agreement. 

The arrangement type is one of those possible/mandatory for the sale of a savings account product. The actual arrangements are a selection of those offered under the savings account product. */
package org.museframework.bian.classes;

public class SavingsAccountArrangement {
    /*Classification of the Savings Account Arrangement according to the subject of the arrangement*/
    private org.museframework.bian.enumerations.Arrangementtypevalues savingsAccountArrangementType;

    /*Reference to the product feature applied in this arrangement */
    private String savingsAccountArrangementProductFeatureType;

    public void setSavingsAccountArrangementType(org.museframework.bian.enumerations.Arrangementtypevalues savingsAccountArrangementType) {
        this.savingsAccountArrangementType = savingsAccountArrangementType;
    }

    public org.museframework.bian.enumerations.Arrangementtypevalues getSavingsAccountArrangementType() {
        return savingsAccountArrangementType;
    }

    public void setSavingsAccountArrangementProductFeatureType(String savingsAccountArrangementProductFeatureType) {
        this.savingsAccountArrangementProductFeatureType = savingsAccountArrangementProductFeatureType;
    }

    public String getSavingsAccountArrangementProductFeatureType() {
        return savingsAccountArrangementProductFeatureType;
    }
}