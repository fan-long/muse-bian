/*An agreement established among the parties to use the payment card for payment. (a proposed definition for BIAN BOM adapted from ECB)*/
package org.museframework.bian.classes;

public class CardPaymentAgreement {
    /**/
    private String cardPaymentInvolvement;

    /*Amount of the payment card.*/
    private String cardAmount;

    /*Currency of the payment card. */
    private String cardCurrency;

    /*Limit specified on a payment card.*/
    private Condition cardLimitTermAndCondition;

    /*Tax specified on a payment card.*/
    private Condition cardTaxTermAndCondition;

    public void setCardPaymentInvolvement(String cardPaymentInvolvement) {
        this.cardPaymentInvolvement = cardPaymentInvolvement;
    }

    public String getCardPaymentInvolvement() {
        return cardPaymentInvolvement;
    }

    public void setCardAmount(String cardAmount) {
        this.cardAmount = cardAmount;
    }

    public String getCardAmount() {
        return cardAmount;
    }

    public void setCardCurrency(String cardCurrency) {
        this.cardCurrency = cardCurrency;
    }

    public String getCardCurrency() {
        return cardCurrency;
    }

    public void setCardLimitTermAndCondition(Condition cardLimitTermAndCondition) {
        this.cardLimitTermAndCondition = cardLimitTermAndCondition;
    }

    public Condition getCardLimitTermAndCondition() {
        return cardLimitTermAndCondition;
    }

    public void setCardTaxTermAndCondition(Condition cardTaxTermAndCondition) {
        this.cardTaxTermAndCondition = cardTaxTermAndCondition;
    }

    public Condition getCardTaxTermAndCondition() {
        return cardTaxTermAndCondition;
    }
}