/*A taxonomical classification of device, according to its characteristics as an item.*/
package org.museframework.bian.classes;

public class DeviceType {
    /*Characteristics of the item that can be used as device

a device has the characteristics of the device type it belongs to,possibly specifiede.g. . a device of the type "cheque book" will have cheques. The device specifies how many and what the number range is.e.g. an electronic banking app consists of software modules, the device specifies what version */
    private String deviceTypeDescription;

    public void setDeviceTypeDescription(String deviceTypeDescription) {
        this.deviceTypeDescription = deviceTypeDescription;
    }

    public String getDeviceTypeDescription() {
        return deviceTypeDescription;
    }
}