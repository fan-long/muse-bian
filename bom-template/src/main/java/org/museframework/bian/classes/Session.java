/*The operation of a system -- typically an automated system -- for a bounded period of time.

Semi-permanent container of state for an information exchange with a system.

Time-bounded, temporal unit of context and continuity, identifying a unit of work.
*/
package org.museframework.bian.classes;

public class Session {
    /*The time the session was active*/
    private String sessionDuration;

    /*Key dates and times for the session*/
    private String sessionDateTime;

    /**/
    private String sessionStatus;

    /*Specifies the classification of session like Customer Contact Session, Channel Operating Session, Device Operating Session, etc.*/
    private String sessionType;

    /**/
    private String sessionScheduel;

    public void setSessionDuration(String sessionDuration) {
        this.sessionDuration = sessionDuration;
    }

    public String getSessionDuration() {
        return sessionDuration;
    }

    public void setSessionDateTime(String sessionDateTime) {
        this.sessionDateTime = sessionDateTime;
    }

    public String getSessionDateTime() {
        return sessionDateTime;
    }

    public void setSessionStatus(String sessionStatus) {
        this.sessionStatus = sessionStatus;
    }

    public String getSessionStatus() {
        return sessionStatus;
    }

    public void setSessionType(String sessionType) {
        this.sessionType = sessionType;
    }

    public String getSessionType() {
        return sessionType;
    }

    public void setSessionScheduel(String sessionScheduel) {
        this.sessionScheduel = sessionScheduel;
    }

    public String getSessionScheduel() {
        return sessionScheduel;
    }
}