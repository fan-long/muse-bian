/*Allocation is the assignment of something or part of something to something to use in a particular way. 

*/
package org.museframework.bian.classes;

public class Allocation {
    /*The amount allocated*/
    private String allocationAmount;

    /*duration for which the allocation is enforced*/
    private String allocationDuration;

    /*Indication if it is an allocation or recall*/
    private String allocationDirection;

    /*A state expressing if the allocation is pending confirmation, allocated, recalled, etc.*/
    private String allocationStatus;

    /**/
    private String allocationDescription;

    public void setAllocationAmount(String allocationAmount) {
        this.allocationAmount = allocationAmount;
    }

    public String getAllocationAmount() {
        return allocationAmount;
    }

    public void setAllocationDuration(String allocationDuration) {
        this.allocationDuration = allocationDuration;
    }

    public String getAllocationDuration() {
        return allocationDuration;
    }

    public void setAllocationDirection(String allocationDirection) {
        this.allocationDirection = allocationDirection;
    }

    public String getAllocationDirection() {
        return allocationDirection;
    }

    public void setAllocationStatus(String allocationStatus) {
        this.allocationStatus = allocationStatus;
    }

    public String getAllocationStatus() {
        return allocationStatus;
    }

    public void setAllocationDescription(String allocationDescription) {
        this.allocationDescription = allocationDescription;
    }

    public String getAllocationDescription() {
        return allocationDescription;
    }
}