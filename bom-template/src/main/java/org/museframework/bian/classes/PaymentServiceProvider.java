/*Involvement of a party as payment service provider in a payment service provider agreement*/
package org.museframework.bian.classes;

public class PaymentServiceProvider {
    /* classification of (packages of) payment services provided to financial institutions 
*/
    private String paymentServiceProviderType;

    /*Identification of the party that is engaged as payment service provider in a payment servie provider agreement*/
    private String paymentServiceProviderIdentifier;

    public void setPaymentServiceProviderType(String paymentServiceProviderType) {
        this.paymentServiceProviderType = paymentServiceProviderType;
    }

    public String getPaymentServiceProviderType() {
        return paymentServiceProviderType;
    }

    public void setPaymentServiceProviderIdentifier(String paymentServiceProviderIdentifier) {
        this.paymentServiceProviderIdentifier = paymentServiceProviderIdentifier;
    }

    public String getPaymentServiceProviderIdentifier() {
        return paymentServiceProviderIdentifier;
    }
}