/*The selling of collateral asset to pay off debts. 


Liquidation in finance and economics is the process of bringing a business to an end and distributing its assets to claimants.
Liquidation is the selling of assets to raise cash, usually to pay off debts. */
package org.museframework.bian.classes;

public class CollateralAssetLiquidation {
    /**/
    private String collateralAssetLiquidationAssessment;

    /**/
    private String collateralAssetLiquidationSchedule;

    public void setCollateralAssetLiquidationAssessment(String collateralAssetLiquidationAssessment) {
        this.collateralAssetLiquidationAssessment = collateralAssetLiquidationAssessment;
    }

    public String getCollateralAssetLiquidationAssessment() {
        return collateralAssetLiquidationAssessment;
    }

    public void setCollateralAssetLiquidationSchedule(String collateralAssetLiquidationSchedule) {
        this.collateralAssetLiquidationSchedule = collateralAssetLiquidationSchedule;
    }

    public String getCollateralAssetLiquidationSchedule() {
        return collateralAssetLiquidationSchedule;
    }
}