/*The valuation of each piece of collateral that is posted.*/
package org.museframework.bian.classes;

public class CollateralAssetValuation {
    /*Valuation status of the collateral asset (ISO20022)*/
    private String collateralAssetValuationStatus;

    /*Valuation date of the collateral asset (ISO20022)*/
    private String collateralAssetValuationDate;

    /*The amount of valuation of a collateral asset*/
    private String collateralAssetValuationAmount;

    /*Currency used for the valuation of a collateral asset. (ISO20022)*/
    private String collateralAssetValuationCurrency;

    public void setCollateralAssetValuationStatus(String collateralAssetValuationStatus) {
        this.collateralAssetValuationStatus = collateralAssetValuationStatus;
    }

    public String getCollateralAssetValuationStatus() {
        return collateralAssetValuationStatus;
    }

    public void setCollateralAssetValuationDate(String collateralAssetValuationDate) {
        this.collateralAssetValuationDate = collateralAssetValuationDate;
    }

    public String getCollateralAssetValuationDate() {
        return collateralAssetValuationDate;
    }

    public void setCollateralAssetValuationAmount(String collateralAssetValuationAmount) {
        this.collateralAssetValuationAmount = collateralAssetValuationAmount;
    }

    public String getCollateralAssetValuationAmount() {
        return collateralAssetValuationAmount;
    }

    public void setCollateralAssetValuationCurrency(String collateralAssetValuationCurrency) {
        this.collateralAssetValuationCurrency = collateralAssetValuationCurrency;
    }

    public String getCollateralAssetValuationCurrency() {
        return collateralAssetValuationCurrency;
    }
}