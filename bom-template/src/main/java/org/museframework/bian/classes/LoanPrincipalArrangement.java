/*An arrangement specisies the amount (initial size of loan) that an entity borrows and must repay.*/
package org.museframework.bian.classes;

public class LoanPrincipalArrangement {
    /*The amount that an entity borrows and must repay.
*/
    private String loanAmount;

    public void setLoanAmount(String loanAmount) {
        this.loanAmount = loanAmount;
    }

    public String getLoanAmount() {
        return loanAmount;
    }
}