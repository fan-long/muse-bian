/*A financial transaction that is the result of fulfillment of a fee arrangement

Example:
- the calculation of fee
- the charge of fee as a debt
- the payment of fee

*/
package org.museframework.bian.classes;

public class FeeTransaction {
    /*Type of the applied Fee*/
    private String feeType;

    /*Total fee charged */
    private String accruedFeeAmount;

    /*Amount of money paid for the charge
Amount of money asked or paid for the charge (ISO20022)
*/
    private String feeAmount;

    public void setFeeType(String feeType) {
        this.feeType = feeType;
    }

    public String getFeeType() {
        return feeType;
    }

    public void setAccruedFeeAmount(String accruedFeeAmount) {
        this.accruedFeeAmount = accruedFeeAmount;
    }

    public String getAccruedFeeAmount() {
        return accruedFeeAmount;
    }

    public void setFeeAmount(String feeAmount) {
        this.feeAmount = feeAmount;
    }

    public String getFeeAmount() {
        return feeAmount;
    }
}