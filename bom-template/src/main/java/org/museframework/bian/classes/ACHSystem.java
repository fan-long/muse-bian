/*A system performing the services of an Automated Clearing House.*/
package org.museframework.bian.classes;

public class ACHSystem {
    /*An identifier that allows to uniquely refer to an ACH Service*/
    private String ACHSystemIdentifier;

    /*A time schedule that the system makes its services availble */
    private String ACHAccessSchedule;

    public void setACHSystemIdentifier(String ACHSystemIdentifier) {
        this.ACHSystemIdentifier = ACHSystemIdentifier;
    }

    public String getACHSystemIdentifier() {
        return ACHSystemIdentifier;
    }

    public void setACHAccessSchedule(String ACHAccessSchedule) {
        this.ACHAccessSchedule = ACHAccessSchedule;
    }

    public String getACHAccessSchedule() {
        return ACHAccessSchedule;
    }
}