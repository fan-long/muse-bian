/*A simplified representation of reality. */
package org.museframework.bian.classes;

public class Model {
    /*The type or category of model  */
    private String modelType;

    /*A description of the model that clarifies the intended analysis/insights provided*/
    private String modelPurpose;

    /**/
    private String modelVersion;

    public void setModelType(String modelType) {
        this.modelType = modelType;
    }

    public String getModelType() {
        return modelType;
    }

    public void setModelPurpose(String modelPurpose) {
        this.modelPurpose = modelPurpose;
    }

    public String getModelPurpose() {
        return modelPurpose;
    }

    public void setModelVersion(String modelVersion) {
        this.modelVersion = modelVersion;
    }

    public String getModelVersion() {
        return modelVersion;
    }
}