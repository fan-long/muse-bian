/*A device (payment instrument) that can be used by its holder to pay for goods and services or to withdraw money. (The dictionary of ECB)

Electronic money product that provides the cardholder with a portable and specialised computer device that typically contains a microprocessor. (ISO20022)

Payment Card means a category of payment instrument that enables the payer to initiate a debit or credit card transaction. (PSD)*/
package org.museframework.bian.classes;

public class PaymentCard {
    /*Type of card, e.g., credit card. (ISO20022)*/
    private String cardType;

    /*Number embossed on a card that links the card to the account owner and account servicer (sometimes called personal account number or PAN). (ISO20022)*/
    private String cardNumber;

    /*Year and month the card is available for use. (ISO20022)*/
    private String cardStartDate;

    /*Year and month the card expires. (ISO20022)*/
    private String cardExpiryDate;

    /*Security code written on the card, sometimes called card security code (CSC). (ISO20022)*/
    private String cardSecurityCode;

    /**/
    private String cardVerificationValue;

    /*The bank identification number uniquely identifies the institution issuing the card. The BIN is key in the process of matching transactions to the issuer of the charge card.*/
    private String bankIdentificationNumber;

    public void setCardType(String cardType) {
        this.cardType = cardType;
    }

    public String getCardType() {
        return cardType;
    }

    public void setCardNumber(String cardNumber) {
        this.cardNumber = cardNumber;
    }

    public String getCardNumber() {
        return cardNumber;
    }

    public void setCardStartDate(String cardStartDate) {
        this.cardStartDate = cardStartDate;
    }

    public String getCardStartDate() {
        return cardStartDate;
    }

    public void setCardExpiryDate(String cardExpiryDate) {
        this.cardExpiryDate = cardExpiryDate;
    }

    public String getCardExpiryDate() {
        return cardExpiryDate;
    }

    public void setCardSecurityCode(String cardSecurityCode) {
        this.cardSecurityCode = cardSecurityCode;
    }

    public String getCardSecurityCode() {
        return cardSecurityCode;
    }

    public void setCardVerificationValue(String cardVerificationValue) {
        this.cardVerificationValue = cardVerificationValue;
    }

    public String getCardVerificationValue() {
        return cardVerificationValue;
    }

    public void setBankIdentificationNumber(String bankIdentificationNumber) {
        this.bankIdentificationNumber = bankIdentificationNumber;
    }

    public String getBankIdentificationNumber() {
        return bankIdentificationNumber;
    }
}