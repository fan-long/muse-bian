/*Posting events to the log.*/
package org.museframework.bian.classes;

public class LogEntry {
    /**/
    private String logEntryDescription;

    /**/
    private String logEntryValueDate;

    /**/
    private String logEntryIdentification;

    /**/
    private String postedEventIdentification;

    /*The status of the log entry (e.g. active, in-suspense etc.)*/
    private String logEntryStatus;

    public void setLogEntryDescription(String logEntryDescription) {
        this.logEntryDescription = logEntryDescription;
    }

    public String getLogEntryDescription() {
        return logEntryDescription;
    }

    public void setLogEntryValueDate(String logEntryValueDate) {
        this.logEntryValueDate = logEntryValueDate;
    }

    public String getLogEntryValueDate() {
        return logEntryValueDate;
    }

    public void setLogEntryIdentification(String logEntryIdentification) {
        this.logEntryIdentification = logEntryIdentification;
    }

    public String getLogEntryIdentification() {
        return logEntryIdentification;
    }

    public void setPostedEventIdentification(String postedEventIdentification) {
        this.postedEventIdentification = postedEventIdentification;
    }

    public String getPostedEventIdentification() {
        return postedEventIdentification;
    }

    public void setLogEntryStatus(String logEntryStatus) {
        this.logEntryStatus = logEntryStatus;
    }

    public String getLogEntryStatus() {
        return logEntryStatus;
    }
}