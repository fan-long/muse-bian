/*A payment, requested from a customer, that is overdue*/
package org.museframework.bian.classes;

public class OpenItem {
    /*The origin of payment that is overdue and considered an open item, i.e. the type of arrangement it is due for.*/
    private String openItemTypeAttribute;

    /*Specific detail of the open item for reference*/
    private String openItemDescription;

    /*Reference to the payment obligation that is overdue
*/
    private String openItemReference;

    public void setOpenItemTypeAttribute(String openItemTypeAttribute) {
        this.openItemTypeAttribute = openItemTypeAttribute;
    }

    public String getOpenItemTypeAttribute() {
        return openItemTypeAttribute;
    }

    public void setOpenItemDescription(String openItemDescription) {
        this.openItemDescription = openItemDescription;
    }

    public String getOpenItemDescription() {
        return openItemDescription;
    }

    public void setOpenItemReference(String openItemReference) {
        this.openItemReference = openItemReference;
    }

    public String getOpenItemReference() {
        return openItemReference;
    }
}