public class MarketAnalysisViewpoint {
    /*Classification of the market research viewpoint.*/
    private String marketAnalysisviewpointType;

    /*Description of the market research viewpoint.*/
    private String marketAnalysisViewpointDescription;

    public void setMarketAnalysisviewpointType(String marketAnalysisviewpointType) {
        this.marketAnalysisviewpointType = marketAnalysisviewpointType;
    }

    public String getMarketAnalysisviewpointType() {
        return marketAnalysisviewpointType;
    }

    public void setMarketAnalysisViewpointDescription(String marketAnalysisViewpointDescription) {
        this.marketAnalysisViewpointDescription = marketAnalysisViewpointDescription;
    }

    public String getMarketAnalysisViewpointDescription() {
        return marketAnalysisViewpointDescription;
    }
}