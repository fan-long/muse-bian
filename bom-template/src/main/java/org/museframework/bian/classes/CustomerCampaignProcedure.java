/*A procedure to handle the deployment, execution and on-going refinement of a customer campaign.*/
package org.museframework.bian.classes;

public class CustomerCampaignProcedure {
    /*Key dates associated with the campaign process (e.g. initiation date, version revision date, completion date)*/
    private String customerCampaignProcedureDate;

    public void setCustomerCampaignProcedureDate(String customerCampaignProcedureDate) {
        this.customerCampaignProcedureDate = customerCampaignProcedureDate;
    }

    public String getCustomerCampaignProcedureDate() {
        return customerCampaignProcedureDate;
    }
}