/*Event which is the result of fulfilling a servicing arrangement. */
package org.museframework.bian.classes;

public class ServicingEvent {
    /*The type of servicing event/activity e.g. self service, contact center assisted,  ATM access*/
    private String servicingEventType;

    public void setServicingEventType(String servicingEventType) {
        this.servicingEventType = servicingEventType;
    }

    public String getServicingEventType() {
        return servicingEventType;
    }
}