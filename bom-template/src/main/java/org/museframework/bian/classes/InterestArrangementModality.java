package org.museframework.bian.classes;

public class InterestArrangementModality {
    /*The type of modality which is used for the interest like derivation modality, calculation modality, payment modality and allocation modality. */
    private org.museframework.bian.enumerations.Modalitytypevalues interestModalityType;

    /*Start date used for calculating accrued interest on debt instruments which are being sold between interest payment dates. Often but not always the same as the issue date and the dated date. (ISO20022)*/
    private String interestAccrualDate;

    public void setInterestModalityType(org.museframework.bian.enumerations.Modalitytypevalues interestModalityType) {
        this.interestModalityType = interestModalityType;
    }

    public org.museframework.bian.enumerations.Modalitytypevalues getInterestModalityType() {
        return interestModalityType;
    }

    public void setInterestAccrualDate(String interestAccrualDate) {
        this.interestAccrualDate = interestAccrualDate;
    }

    public String getInterestAccrualDate() {
        return interestAccrualDate;
    }
}