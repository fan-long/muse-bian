/*An arrangement which specifies to charge fees when banking transactions did take place, accompanied by the modalities to calculate and charge the fees and the related terms and conditions. */
package org.museframework.bian.classes;

public class FeeArrangement {
    /*A classification of fee according to the type of service. 
Type of service for which a charge is asked or paid. (ISO20022)*/
    private String feeType;

    /*Base amount that would be applied when the service is requested. 

*/
    private String feeAmount;

    /*Rate used to calculate the amount of the adjustment, allowance, charge or fee. (ISO20022)*/
    private String feeRate;

    /*Period during which the fee is applicable. (ISO20022)*/
    private String feeEffectivePeriod;

    /*Date on which the fee needs to be paid.
- It is not nessessary a date and could be  expressed as a calander or formula or events that would be fulfiled. */
    private String feeDueDate;

    /*An arranged FeeRatePlan
*/
    private String feePlan;

    public void setFeeType(String feeType) {
        this.feeType = feeType;
    }

    public String getFeeType() {
        return feeType;
    }

    public void setFeeAmount(String feeAmount) {
        this.feeAmount = feeAmount;
    }

    public String getFeeAmount() {
        return feeAmount;
    }

    public void setFeeRate(String feeRate) {
        this.feeRate = feeRate;
    }

    public String getFeeRate() {
        return feeRate;
    }

    public void setFeeEffectivePeriod(String feeEffectivePeriod) {
        this.feeEffectivePeriod = feeEffectivePeriod;
    }

    public String getFeeEffectivePeriod() {
        return feeEffectivePeriod;
    }

    public void setFeeDueDate(String feeDueDate) {
        this.feeDueDate = feeDueDate;
    }

    public String getFeeDueDate() {
        return feeDueDate;
    }

    public void setFeePlan(String feePlan) {
        this.feePlan = feePlan;
    }

    public String getFeePlan() {
        return feePlan;
    }
}