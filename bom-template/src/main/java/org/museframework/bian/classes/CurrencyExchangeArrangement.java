/*An arrangement whitin the CurrencyExchangeAgreement*/
package org.museframework.bian.classes;

public class CurrencyExchangeArrangement {
    /*The type of the currency exchange.*/
    private org.museframework.bian.enumerations.Exchangeratetypevalues exchangeRateType;

    /*The value of one currency expressed in relation to another currency. ExchangeRate expresses the ratio between UnitCurrency and QuotedCurrency (ExchangeRate = UnitCurrency/QuotedCurrency). (ISO20022)*/
    private String exchangeRate;

    /*Amount in its original currency when conversion from/into another currency has occurred (ISO20022)*/
    private String originalAmount;

    /*Currency in which the rate of exchange is expressed in a currency exchange. In the example 1GBP = xxxCUR, the unit currency is GBP . (ISO20022)*/
    private String unitCurrency;

    /*Currency into which the base currency is converted, in a currency exchange. (ISO20022)*/
    private String quotedCurrency;

    /*Currency of the amount to be converted in a currency conversion. (ISO20022)*/
    private String sourceCurrency;

    /*Currency into which an amount is to be converted in a currency conversion. (ISO20022)*/
    private String targetCurrency;

    public void setExchangeRateType(org.museframework.bian.enumerations.Exchangeratetypevalues exchangeRateType) {
        this.exchangeRateType = exchangeRateType;
    }

    public org.museframework.bian.enumerations.Exchangeratetypevalues getExchangeRateType() {
        return exchangeRateType;
    }

    public void setExchangeRate(String exchangeRate) {
        this.exchangeRate = exchangeRate;
    }

    public String getExchangeRate() {
        return exchangeRate;
    }

    public void setOriginalAmount(String originalAmount) {
        this.originalAmount = originalAmount;
    }

    public String getOriginalAmount() {
        return originalAmount;
    }

    public void setUnitCurrency(String unitCurrency) {
        this.unitCurrency = unitCurrency;
    }

    public String getUnitCurrency() {
        return unitCurrency;
    }

    public void setQuotedCurrency(String quotedCurrency) {
        this.quotedCurrency = quotedCurrency;
    }

    public String getQuotedCurrency() {
        return quotedCurrency;
    }

    public void setSourceCurrency(String sourceCurrency) {
        this.sourceCurrency = sourceCurrency;
    }

    public String getSourceCurrency() {
        return sourceCurrency;
    }

    public void setTargetCurrency(String targetCurrency) {
        this.targetCurrency = targetCurrency;
    }

    public String getTargetCurrency() {
        return targetCurrency;
    }
}