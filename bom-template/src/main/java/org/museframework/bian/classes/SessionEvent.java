/*An event having to do with a session.*/
package org.museframework.bian.classes;

public class SessionEvent {
    /*The kind of event happening during a session

events are being logged 

e.g. login, failure...*/
    private String sessionEventyType;

    /*Date/Time the event occurred (or was detected/logged)*/
    private String sessionEventDateTime;

    public void setSessionEventyType(String sessionEventyType) {
        this.sessionEventyType = sessionEventyType;
    }

    public String getSessionEventyType() {
        return sessionEventyType;
    }

    public void setSessionEventDateTime(String sessionEventDateTime) {
        this.sessionEventDateTime = sessionEventDateTime;
    }

    public String getSessionEventDateTime() {
        return sessionEventDateTime;
    }
}