/*An instruction to request the sweep (execution of structured sweeps to and from the facility to a linked account)
*/
package org.museframework.bian.classes;

public class SweepInstruction {
    /*The facility that the sweep is made to/from*/
    private String sweepTargetAccount;

    /*The amount transferred to or from the account*/
    private String sweepAmount;

    /*The date and time the sweep is initiated*/
    private String sweepExecutionDate;

    public void setSweepTargetAccount(String sweepTargetAccount) {
        this.sweepTargetAccount = sweepTargetAccount;
    }

    public String getSweepTargetAccount() {
        return sweepTargetAccount;
    }

    public void setSweepAmount(String sweepAmount) {
        this.sweepAmount = sweepAmount;
    }

    public String getSweepAmount() {
        return sweepAmount;
    }

    public void setSweepExecutionDate(String sweepExecutionDate) {
        this.sweepExecutionDate = sweepExecutionDate;
    }

    public String getSweepExecutionDate() {
        return sweepExecutionDate;
    }
}