/*A classification to distinguish between instances of systems regarding to their capability for payment/fund transfer.*/
package org.museframework.bian.classes;

public class PaymentSystemClass {
    /*Identifier of the payment system class.
*/
    private String paymentSystemClassIdentifier;

    /*Type of the payment system class.*/
    private String paymentSystemClassType;

    /*Properties of the payment system class.*/
    private String paymentSystemClassProperties;

    public void setPaymentSystemClassIdentifier(String paymentSystemClassIdentifier) {
        this.paymentSystemClassIdentifier = paymentSystemClassIdentifier;
    }

    public String getPaymentSystemClassIdentifier() {
        return paymentSystemClassIdentifier;
    }

    public void setPaymentSystemClassType(String paymentSystemClassType) {
        this.paymentSystemClassType = paymentSystemClassType;
    }

    public String getPaymentSystemClassType() {
        return paymentSystemClassType;
    }

    public void setPaymentSystemClassProperties(String paymentSystemClassProperties) {
        this.paymentSystemClassProperties = paymentSystemClassProperties;
    }

    public String getPaymentSystemClassProperties() {
        return paymentSystemClassProperties;
    }
}