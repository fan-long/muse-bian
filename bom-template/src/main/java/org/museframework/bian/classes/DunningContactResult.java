/*Relationship between a repayment transaction and the dunning contact it resulted from*/
package org.museframework.bian.classes;

public class DunningContactResult {
    /*Reference to the repayment transaction that resulted from the dunning contact*/
    private String dunningContactResultingRepaymentTransactionReference;

    /*Reference to the dunning contact that triggered the repayment*/
    private String dunningContactResultTriggeringContact;

    public void setDunningContactResultingRepaymentTransactionReference(String dunningContactResultingRepaymentTransactionReference) {
        this.dunningContactResultingRepaymentTransactionReference = dunningContactResultingRepaymentTransactionReference;
    }

    public String getDunningContactResultingRepaymentTransactionReference() {
        return dunningContactResultingRepaymentTransactionReference;
    }

    public void setDunningContactResultTriggeringContact(String dunningContactResultTriggeringContact) {
        this.dunningContactResultTriggeringContact = dunningContactResultTriggeringContact;
    }

    public String getDunningContactResultTriggeringContact() {
        return dunningContactResultTriggeringContact;
    }
}