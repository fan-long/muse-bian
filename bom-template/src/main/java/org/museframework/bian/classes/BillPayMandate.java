/*A mandate to a third party to pay a partys bills*/
package org.museframework.bian.classes;

public class BillPayMandate {
    /*Identification of a bill pay mandate*/
    private String billPayMandateIdentification;

    /*Modaliteis of a bill pay mandate*/
    private String billPayServiceConfiguration;

    public void setBillPayMandateIdentification(String billPayMandateIdentification) {
        this.billPayMandateIdentification = billPayMandateIdentification;
    }

    public String getBillPayMandateIdentification() {
        return billPayMandateIdentification;
    }

    public void setBillPayServiceConfiguration(String billPayServiceConfiguration) {
        this.billPayServiceConfiguration = billPayServiceConfiguration;
    }

    public String getBillPayServiceConfiguration() {
        return billPayServiceConfiguration;
    }
}