/*A registry to record some specific items. */
package org.museframework.bian.classes;

public class Directory {
    /**/
    private String directoryVersion;

    /**/
    private String directoryConfiguration;

    /**/
    private String directoryValidPeriod;

    /**/
    private String directoryStatus;

    public void setDirectoryVersion(String directoryVersion) {
        this.directoryVersion = directoryVersion;
    }

    public String getDirectoryVersion() {
        return directoryVersion;
    }

    public void setDirectoryConfiguration(String directoryConfiguration) {
        this.directoryConfiguration = directoryConfiguration;
    }

    public String getDirectoryConfiguration() {
        return directoryConfiguration;
    }

    public void setDirectoryValidPeriod(String directoryValidPeriod) {
        this.directoryValidPeriod = directoryValidPeriod;
    }

    public String getDirectoryValidPeriod() {
        return directoryValidPeriod;
    }

    public void setDirectoryStatus(String directoryStatus) {
        this.directoryStatus = directoryStatus;
    }

    public String getDirectoryStatus() {
        return directoryStatus;
    }
}