/*Configuration for deployment of something like Product Design or Model Design*/
package org.museframework.bian.classes;

public class DeploymentConfiguration {
    /*Reference to the deployment task */
    private String deploymentTask;

    /*Party who plays the role in the deployment.
*/
    private String deploymentInvolvement;

    public void setDeploymentTask(String deploymentTask) {
        this.deploymentTask = deploymentTask;
    }

    public String getDeploymentTask() {
        return deploymentTask;
    }

    public void setDeploymentInvolvement(String deploymentInvolvement) {
        this.deploymentInvolvement = deploymentInvolvement;
    }

    public String getDeploymentInvolvement() {
        return deploymentInvolvement;
    }
}