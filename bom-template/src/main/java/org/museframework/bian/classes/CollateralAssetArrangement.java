/*An arrangement within an agreement where an Asset is provided as security for a payment or repayment of an obligation. (BIAN)
*/
package org.museframework.bian.classes;

public class CollateralAssetArrangement {
    /*Date when the asset was provided as collateral*/
    private String collateralPledgedDate;

    /*Classification of the collateral according to a collateral mechanism classification hierarchy.
Examples: mortgage, blocked deposit, a blocked securities portfolio, pawn, etc.*/
    private String collateralType;

    /*A Value amount that is pledged to secure the payments or repayments.*/
    private String collateralValuePledged;

    /*Value of the collateral as an amount. (ISO20022)*/
    private String collateralAmount;

    public void setCollateralPledgedDate(String collateralPledgedDate) {
        this.collateralPledgedDate = collateralPledgedDate;
    }

    public String getCollateralPledgedDate() {
        return collateralPledgedDate;
    }

    public void setCollateralType(String collateralType) {
        this.collateralType = collateralType;
    }

    public String getCollateralType() {
        return collateralType;
    }

    public void setCollateralValuePledged(String collateralValuePledged) {
        this.collateralValuePledged = collateralValuePledged;
    }

    public String getCollateralValuePledged() {
        return collateralValuePledged;
    }

    public void setCollateralAmount(String collateralAmount) {
        this.collateralAmount = collateralAmount;
    }

    public String getCollateralAmount() {
        return collateralAmount;
    }
}